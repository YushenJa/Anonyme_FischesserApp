/**
 * Das Enum Esstyp repräsentiert verschiedene Essgewohnheiten.
 * Es enthält die folgenden Werte:
 * - VEGANER: Ein Esser, der ausschließlich pflanzliche Produkte verzehrt.
 * - VEGETARIER: Ein Esser, der pflanzliche Produkte sowie Milchprodukte und Eier verzehrt, aber kein Fleisch.
 * - FISCHFRESSER: Ein Esser, der sowohl pflanzliche Produkte als auch Fische verzehrt.
 */
public enum Esstyp {
    VEGANER, 
    VEGETARIER, 
    FISCHFRESSER
}